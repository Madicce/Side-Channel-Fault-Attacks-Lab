static const uint8_t RCON[11] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10,
                                 0x20, 0x40, 0x80, 0x1B, 0x36};

static void rot_word(uint8_t *w) {
  uint8_t tmp = w[0];
  w[0] = w[1];
  w[1] = w[2];
  w[2] = w[3];
  w[3] = tmp;
}

static void sub_word(uint8_t *w) {
  w[0] = SBOX[w[0]];
  w[1] = SBOX[w[1]];
  w[2] = SBOX[w[2]];
  w[3] = SBOX[w[3]];
}

void aes_key_expansion(const uint8_t *key, uint8_t *round_keys) {
  int i;
  uint8_t temp[4];

  // Copy original key (first 16 bytes)
  for (i = 0; i < 16; i++) {
    round_keys[i] = key[i];
  }

  for (i = 16; i < 176; i += 4) {

    // copy previous word
    temp[0] = round_keys[i - 4];
    temp[1] = round_keys[i - 3];
    temp[2] = round_keys[i - 2];
    temp[3] = round_keys[i - 1];

    // every 16 bytes (new round)
    if ((i % 16) == 0) {
      rot_word(temp);
      sub_word(temp);
      temp[0] ^= RCON[i / 16];
    }

    round_keys[i + 0] = round_keys[i - 16] ^ temp[0];
    round_keys[i + 1] = round_keys[i - 15] ^ temp[1];
    round_keys[i + 2] = round_keys[i - 14] ^ temp[2];
    round_keys[i + 3] = round_keys[i - 13] ^ temp[3];
  }
}

static void add_round_key(uint8_t *state, const uint8_t *roundkey) {
  for (int i = 0; i < 16; i++) {
    state[i] ^= roundkey[i];
  }
}

static void s_box(uint8_t *state) {
  for (int i = 0; i < 16; i++) {
    state[i] = SBOX[state[i]];
  }
}

static void inv_s_box(uint8_t *state) {
  for (int i = 0; i < 16; i++) {
    state[i] = INV_SBOX[state[i]];
  }
}

static void shift_rows(uint8_t *state) {
  uint8_t tmp;

  // row 1 (shift left 1)
  tmp = state[1];
  state[1] = state[5];
  state[5] = state[9];
  state[9] = state[13];
  state[13] = tmp;

  // row 2 (shift left 2)
  tmp = state[2];
  state[2] = state[10];
  state[10] = tmp;

  tmp = state[6];
  state[6] = state[14];
  state[14] = tmp;

  // row 3 (shift left 3 = right 1)
  tmp = state[3];
  state[3] = state[15];
  state[15] = state[11];
  state[11] = state[7];
  state[7] = tmp;
}

static void inv_shift_rows(uint8_t *state) {
  uint8_t tmp;

  // row 1 (right 1)
  tmp = state[13];
  state[13] = state[9];
  state[9] = state[5];
  state[5] = state[1];
  state[1] = tmp;

  // row 2 (right 2)
  tmp = state[2];
  state[2] = state[10];
  state[10] = tmp;

  tmp = state[6];
  state[6] = state[14];
  state[14] = tmp;

  // row 3 (right 3 = left 1)
  tmp = state[3];
  state[3] = state[7];
  state[7] = state[11];
  state[11] = state[15];
  state[15] = tmp;
}

static uint8_t xtime(uint8_t x) { return (x << 1) ^ ((x >> 7) * 0x1b); }

static uint8_t mul(uint8_t x, uint8_t y) {
  uint8_t res = 0;

  while (y) {
    if (y & 1)
      res ^= x;
    x = xtime(x);
    y >>= 1;
  }
  return res;
}

static void inv_mix_columns(uint8_t *state) {
  uint8_t a, b, c, d;

  for (int i = 0; i < 4; i++) {
    int col = 4 * i;

    a = state[col];
    b = state[col + 1];
    c = state[col + 2];
    d = state[col + 3];

    state[col] = mul(a, 14) ^ mul(b, 11) ^ mul(c, 13) ^ mul(d, 9);
    state[col + 1] = mul(a, 9) ^ mul(b, 14) ^ mul(c, 11) ^ mul(d, 13);
    state[col + 2] = mul(a, 13) ^ mul(b, 9) ^ mul(c, 14) ^ mul(d, 11);
    state[col + 3] = mul(a, 11) ^ mul(b, 13) ^ mul(c, 9) ^ mul(d, 14);
  }
}

static void mix_columns(uint8_t *state) {
  uint8_t t, tmp, tm;

  for (int c = 0; c < 4; c++) {
    int i = 4 * c;

    t = state[i];
    tmp = state[i] ^ state[i + 1] ^ state[i + 2] ^ state[i + 3];

    tm = state[i] ^ state[i + 1];
    tm = xtime(tm);
    state[i] ^= tm ^ tmp;

    tm = state[i + 1] ^ state[i + 2];
    tm = xtime(tm);
    state[i + 1] ^= tm ^ tmp;

    tm = state[i + 2] ^ state[i + 3];
    tm = xtime(tm);
    state[i + 2] ^= tm ^ tmp;

    tm = state[i + 3] ^ t;
    tm = xtime(tm);
    state[i + 3] ^= tm ^ tmp;
  }
}

void aes_encrypt(uint8_t *state, const uint8_t *round_keys) {
  int round;

  // -------------------------
  // Round 0
  // -------------------------
  add_round_key(state, round_keys);

  // -------------------------
  // Rounds 1–9
  // -------------------------
  for (round = 1; round < 10; round++) {
    s_box(state);
    shift_rows(state);
    mix_columns(state);
    add_round_key(state, round_keys + (16 * round));
  }

  // -------------------------
  // Round 10 (no MixColumns)
  // -------------------------
  s_box(state);
  shift_rows(state);
  add_round_key(state, round_keys + 160);
}

void aes_decrypt(uint8_t *state, const uint8_t *round_keys) {
  int round;

  // -------------------------
  // Round 0 (last key)
  // -------------------------
  add_round_key(state, round_keys + 160);

  // -------------------------
  // Rounds 9 → 1
  // -------------------------
  for (round = 9; round > 0; round--) {

    inv_shift_rows(state);
    inv_s_box(state);
    add_round_key(state, round_keys + (16 * round));
    inv_mix_columns(state);
  }

  // -------------------------
  // Final round
  // -------------------------
  inv_shift_rows(state);
  inv_s_box(state);
  add_round_key(state, round_keys);
}